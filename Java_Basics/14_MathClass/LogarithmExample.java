public class LogarithmExample {
    public static void main(String[] args) {
        double number = 100;

        System.out.println("Natural Logarithm of " + number + ": " + Math.log(number)); // 4.605170186
        System.out.println("Logarithm base 10 of " + number + ": " + Math.log10(number)); // 2.0
    }
}
