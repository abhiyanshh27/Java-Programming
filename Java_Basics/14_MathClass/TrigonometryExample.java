public class TrigonometryExample {
    public static void main(String[] args) {
        double angle = Math.toRadians(30); // Converting degrees to radians

        System.out.println("Sin(30°): " + Math.sin(angle));  // 0.5
        System.out.println("Cos(30°): " + Math.cos(angle));  // 0.86602540378
        System.out.println("Tan(30°): " + Math.tan(angle));  // 0.57735026919
    }
}
