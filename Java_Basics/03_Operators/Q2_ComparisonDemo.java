// Q2_ComparisonDemo.java
// Compare two numbers using relational operators

public class Q2_ComparisonDemo {
    public static void main(String[] args) {
        int x = 15, y = 20;

        System.out.println("x > y: " + (x > y));
        System.out.println("x < y: " + (x < y));
        System.out.println("x == y: " + (x == y));
    }
}
