// PrintExplanation.java
// This file explains the basic print statements in Java

public class PrintExplanation {
    public static void main(String[] args) {
        // println prints text and moves to the next line
        System.out.println("Hello, World!");

        // print prints text on the same line
        System.out.print("Hello");
        System.out.print(" World!");

        // You can also print variables
        int age = 20;
        System.out.println("\nAge is: " + age);
    }
}
